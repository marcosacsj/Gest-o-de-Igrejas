    private void PopulaTab() {
          DefaultTableModel modelo1 = (DefaultTableModel) TabDizimo.getModel();
      String sSql = new String();
      String nome = new String();
      int linha=0;
  
      con=new ConectaBanco();
       
       sSql="SELECT * FROM dizimo order by nome,ano desc;";
        con.conecta();
        try {
            con.executaSelect(sSql);
        } catch (SQLException ex) {
            InOut.MsgDeErro(null, ex.getMessage());
        }
        lRes=con.resultset; 
        System.out.println(lRes);
        System.out.println(sSql);
        try {                   
            modelo1.setRowCount(0);
            while (lRes.next()) {
             
                
                modelo1.addRow(new Object[]{
                            lRes.getString("NOME"),
                        lRes.getString("JANEIRO"),
                        lRes.getString("FEVEREIRO"),
                        lRes.getString("MARCO"),
                        lRes.getString("ABRIL"),
                        lRes.getString("MAIO"),
                        lRes.getString("JUNHO"),
                        lRes.getString("JULHO"),
                        lRes.getString("AGOSTO"),
                        lRes.getString("SETEMBRO"),
                        lRes.getString("OUTUBRO"),
                        lRes.getString("NOVEMBRO"),
                        lRes.getString("DEZEMBRO"),
                        lRes.getString("ANO")
                           });
            }
            TabDizimo.setModel(modelo1);
        } catch (SQLException ex) {
            Logger.getLogger(Dizimos.class.getName()).log(Level.SEVERE, null, ex);
        }
     con.desconecta();        
    }

